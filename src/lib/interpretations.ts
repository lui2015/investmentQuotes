export function getInterpretation(quote: Quote): Interpretation {
  // 1) 精准签名匹配
  for (const sig of bySignature) {
    if (sig.master_id !== quote.master_id) continue;
    const hit = sig.keywords.some((kw) => quote.content_cn.includes(kw));
    if (hit) {
      const base = sig.data;
      return {
        ...base,
        masterView: base.masterView ?? masterViewByMaster[quote.master_id],
      };
    }
  }

  // 2) 标签通用解读（取命中的第一个标签）
  const tagId = quote.tags?.find((t) => byTag[t.id])?.id;
  if (tagId && byTag[tagId]) {
    return {
      ...byTag[tagId],
      masterView: masterViewByMaster[quote.master_id],
    };
  }

  // 3) 最后兜底
  return {
    core:
      "这句话凝结了作者多年投资经验的直觉——它可能看似朴素，但在真实市场里能做到的人极少。尝试把它和你最近的一次投资决策联系起来，看看你是否真的贯彻了它。",
    practice: [
      "把这句话写下来贴在显眼的地方，每次下单前看一眼。",
      "下一次投资决策时，问自己：『这句话如果应用在这里，我该怎么做？』",
      "每周复盘：我这周的操作里，哪些符合这句话、哪些违背了？",
      "把这句话分享给一个会给你坦诚反馈的朋友，让他帮你监督。",
    ],
    story:
      "好名言之所以经典，是因为它们在不同的时代、不同的市场里反复被验证。把它当成『镜子』而不是『金句』——它不是用来点赞的，而是用来每天照一照自己是不是走偏了。",
    masterView: masterViewByMaster[quote.master_id],
  };
}
